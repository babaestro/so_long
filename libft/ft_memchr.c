#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*d;

	i = 0;
	d = (unsigned char *)s;
	while (n--)
	{
		if (d[i] == (unsigned char) c)
			return ((void *)d + i);
		i++;
	}
	return (NULL);
}
